package tinkoff.houses;

public class Road {
    public int first;
    public int second;
    public int h;

    public int getSecond() {
        return second;
    }

    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }


    public void setSecond(int second) {
        this.second = second;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }
}
